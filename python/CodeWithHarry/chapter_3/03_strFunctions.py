name = "bobby chaudhary"

print(len(name))
print(name.startswith("bob")) # give output in TRUE, FALSE
print(name.endswith("bby"))
print(name.capitalize()) # capitalize only the FIRST characters
print(name.upper()) # make all in UPPER case
print(name.lower())  # make all in LOWER case
print(name.count("b")) # count the NUMBER OF SAME characters
print(name.find("chaudhary")) # print the index no of a first characters of a word
print(name.replace("chaudhary", "jaat"))